const I18N = {
  currentLang: "en",
  isTranslating: false,
  observer: null,

  init: () => {
    // Read saved language preference
    const savedLang = localStorage.getItem("vikas_lang") || "en";
    I18N.setLanguage(savedLang);

    // Setup dynamic MutationObserver to translate any dynamic/js elements that are created or changed
    if (typeof document !== "undefined" && document.body) {
      I18N.observer = new MutationObserver(() => {
        if (I18N.isTranslating) return;
        if (I18N.currentLang !== "en") {
          I18N.isTranslating = true;
          try {
            if (I18N.observer) I18N.observer.disconnect();
            I18N.translateDOM();
            if (I18N.observer) {
              I18N.observer.observe(document.body, {
                childList: true,
                subtree: true,
                characterData: true
              });
            }
          } catch (e) {
            console.error("MutationObserver translation failure", e);
          }
          I18N.isTranslating = false;
        }
      });
      I18N.observer.observe(document.body, {
        childList: true,
        subtree: true,
        characterData: true
      });
    }
  },

  setLanguage: (lang) => {
    let targetLang = lang;
    if (!TRANSLATIONS[targetLang]) {
      targetLang = "en";
    }
    I18N.currentLang = targetLang;
    localStorage.setItem("vikas_lang", targetLang);

    I18N.isTranslating = true;
    try {
      if (I18N.observer) I18N.observer.disconnect();

      // Apply translations to all data-i18n elements
      const elements = document.querySelectorAll("[data-i18n]");
      elements.forEach(el => {
        const key = el.getAttribute("data-i18n");
        const translation = TRANSLATIONS[targetLang][key];
        if (translation !== undefined) {
          // Preserve any icon elements inside the element if present
          const icon = el.querySelector("i") || el.querySelector("[data-lucide]");
          if (icon) {
            // If there is an icon, we update only the text node or target the text span
            const span = el.querySelector("span");
            if (span) {
              span.innerText = translation;
            } else {
              // Re-render text preserving icon
              const iconHtml = icon.outerHTML;
              el.innerHTML = `${translation} ${iconHtml}`;
            }
          } else {
            el.innerText = translation;
          }
        }
      });

      // Apply placeholder translations via standard data-i18n-placeholder attributes
      const inputs = document.querySelectorAll("[data-i18n-placeholder]");
      inputs.forEach(input => {
        const key = input.getAttribute("data-i18n-placeholder");
        const translation = TRANSLATIONS[targetLang][key];
        if (translation !== undefined) {
          input.placeholder = translation;
        }
      });

      // Update active state in dropdown UI
      const selectedLabel = document.getElementById("selected-lang-label");
      if (selectedLabel) {
        const labels = { en: "English", hi: "हिंदी" };
        selectedLabel.innerText = labels[targetLang] || "English";
      }

      // Translate rest of the visible DOM
      I18N.translateDOM();

      if (I18N.observer && typeof document !== "undefined" && document.body) {
        I18N.observer.observe(document.body, {
          childList: true,
          subtree: true,
          characterData: true
        });
      }
    } catch (e) {
      console.error("DOM translation failure", e);
    }
    I18N.isTranslating = false;

    // Refresh lucide icons in case outerHTML updates destroyed event listeners
    if (window.lucide) {
      window.lucide.createIcons();
    }

    // Refresh password strength text if function is loaded
    if (window.updatePasswordStrength) {
      const pwdInput = document.getElementById('login-password');
      if (pwdInput) {
        window.updatePasswordStrength(pwdInput.value);
      }
    }
  },

  translateDOM: (root) => {
    const lang = I18N.currentLang || "en";
    const container = root || document.body;
    if (!container) return;

    // 1. Translate elements with no children (leaves) or direct text elements
    const els = container.querySelectorAll("h1, h2, h3, h4, h5, h6, p, span, label, th, td, button, a, option, div");
    els.forEach(el => {
      // Skip if marked with data-i18n to avoid conflicting
      if (el.hasAttribute("data-i18n")) return;
      if (["SCRIPT", "STYLE", "IFRAME", "NOSCRIPT"].includes(el.tagName)) return;

      if (el.children.length === 0) {
        let orig = el.getAttribute("data-orig-text");
        if (orig === null) {
          orig = el.textContent.trim();
          el.setAttribute("data-orig-text", orig);
        }

        if (lang === "en") {
          el.textContent = orig;
        } else {
          const match = DIRECT_MAPPINGS[orig];
          if (match && match[lang]) {
            el.textContent = match[lang];
          }
        }
      } else {
        // If it has children, parse child nodes to safely translate only raw text nodes
        el.childNodes.forEach(child => {
          if (child.nodeType === Node.TEXT_NODE) {
            const trimmed = child.nodeValue.trim();
            if (!trimmed) return;

            let orig = child.originalText;
            if (orig === undefined) {
              orig = trimmed;
              child.originalText = orig;
            }

            if (lang === "en") {
              child.nodeValue = child.nodeValue.replace(trimmed, orig);
            } else {
              const match = DIRECT_MAPPINGS[orig];
              if (match && match[lang]) {
                child.nodeValue = child.nodeValue.replace(trimmed, match[lang]);
              }
            }
          }
        });
      }
    });

    // 2. Translate input & textarea placeholders
    const inputs = container.querySelectorAll("input[placeholder], textarea[placeholder]");
    inputs.forEach(input => {
      if (input.hasAttribute("data-i18n-placeholder")) return;

      let orig = input.getAttribute("data-orig-placeholder");
      if (orig === null) {
        orig = input.placeholder || "";
        input.setAttribute("data-orig-placeholder", orig);
      }

      if (lang === "en") {
        input.placeholder = orig;
      } else {
        const match = DIRECT_MAPPINGS[orig];
        if (match && match[lang]) {
          input.placeholder = match[lang];
        }
      }
    });
  },

  translate: (key) => {
    const lang = I18N.currentLang || "en";
    if (TRANSLATIONS[lang] && TRANSLATIONS[lang][key] !== undefined) {
      return TRANSLATIONS[lang][key];
    }
    const match = DIRECT_MAPPINGS[key];
    if (match && match[lang] !== undefined) {
      return match[lang];
    }
    return key;
  },

  toggleDropdown: () => {
    const menu = document.getElementById("lang-dropdown-menu");
    if (menu) {
      menu.classList.toggle("hidden");
    }
  }
};

// Auto close dropdown when clicking outside
document.addEventListener("click", (e) => {
  const dropdown = document.getElementById("lang-dropdown-container");
  const menu = document.getElementById("lang-dropdown-menu");
  if (dropdown && menu && !dropdown.contains(e.target)) {
    menu.classList.add("hidden");
  }
});

window.I18N = I18N;
